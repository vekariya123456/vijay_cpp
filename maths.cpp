#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    cout<<"<--********CEIL********-->"<<endl;
    cout <<"Ceil number is:" << ceil(3.5) << endl;
    cout <<"Ceil number is:" << ceil(3.1) << endl;
    cout <<"Ceil number is:" << ceil(3.9) << endl;
    
    cout<<"\n<--********FLOOR********-->"<<endl;
    cout <<"Floor number is:" << floor(5.9) << endl;
    cout <<"Floor number is:" << floor(5.2) << endl;
    cout <<"Floor number is:" << floor(5.9) << endl;
  
    cout<<"\n<--********ROUND********-->"<<endl;
    cout <<"Round number is:" << round(8.9) << endl;
    cout <<"Round number is:" << round(8.5) << endl;
    cout <<"Round number is:" << round(8.2) << endl;
    
    cout<<"\n<--********SQRT********-->"<<endl;
    cout <<"SQRT number is:"  << sqrt(16) << endl;
    cout <<"SQRT number is:"  << sqrt(4) << endl;
    
    cout<<"\n<--********POWER********-->"<<endl;
    cout <<"Power number is:" << pow(4,2) << endl;
    cout <<"Power number is:" << pow(8,3) << endl;
    
    cout<<"\n<--********ABS********-->"<<endl; 
    cout <<"ABS number is:"   << abs(-8) << endl;
    cout <<"ABS number is:"   << abs(-15) << endl;
    cout <<"ABS number is:"   << abs(89) << endl;
        
}